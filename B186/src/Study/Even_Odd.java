package Study;

public class Even_Odd 
{
	public static void main(String []args)
	{
		int count=0;
		for(int i=1;i<=10;i++) 
		{
			if(i%2==0)
		{
			System.out.println("Even no"+i);
			count++;
			System.out.println("Even no count is:"+count);
		}
		else
		 {
			System.out.println("odd no"+i);
			count++;
			System.out.println("Odd no count is:"+count);
		 }
			
		}
	}
}
	
	
	
		
		
	
	


