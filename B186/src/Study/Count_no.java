package Study;

public class Count_no
{
	public static void main(String[] args)
	{
		int[]num= {1,2,3,4,5,6,7};
		int count=0;
		
		for(int i=0;i<num.length;i++)
		{
			System.out.println("Count of number is:"+count);
			count++;
		}
		System.out.println("Total count of element is:"+count);
		
	}

}

